    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            socket = new Socket(txtIp.getText().trim(),Integer.valueOf(txtportf.getText()) );
            txt.append("Bağlantı başarılı ... \n");
            out = new ObjectOutputStream(socket.getOutputStream());
            Data data = new Data();
            data.setStatus("yeni");
            data.setName("Ömer's pc");
            out.writeObject(data);
            out.flush();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e, "Hata!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            JFileChooser ch = new JFileChooser();
            int c = ch.showOpenDialog(this);
            if (c == JFileChooser.APPROVE_OPTION) {
                File f = ch.getSelectedFile();
                FileInputStream in = new FileInputStream(f);
                byte b[] = new byte[in.available()];
                in.read(b);
                Data data = new Data();
                data.setStatus(jComboBox1.getSelectedItem() + "");
                
                data.setName(f.getName());
                data.setFile(b);
                out.writeObject(data);
                out.flush();
                txt.append("Bir adet dosya gonderildi ../n");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e, "Hata!", JOptionPane.ERROR_MESSAGE);
        }
      
    }//GEN-LAST:event_jButton2ActionPerformed

